package aula03;

public class exemplo1 {

	public static void main(String[] args) {
		int idade = 15; // inicializa a variável idade com 15
		while(idade < 18) { // executa enquanto a idade for menor que 18
			System.out.println(idade); // exibe o valor atual da idade
			idade = idade + 1; // incrementa a idade em 1 a cada iteração
		}
	}
}
