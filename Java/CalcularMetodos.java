package metodos;

public class CalcularMetodos {

	public int somar(int a, int b) { // método que recebe dois números inteiros
		return a + b; // retorna a soma dos dois números
	}

	public int somar(int a, int b, int c) { // método que recebe três números inteiros
		return a + b + c; // retorna a soma dos três números
	}
}
