package metodos;

public class JavaMetodos {

	public int idade() { // método que retorna a idade como inteiro
		return 25;
	}

	public double nota() { // método que retorna a nota como número decimal
		return 8.5;
	}

	public String nome() { // método que retorna o nome como string
		return "Júlia";
	}

	public boolean estado() { // método que retorna um valor booleano
		return true;
	}

	public void imprimir() { // método que não retorna valor, apenas imprime
		System.out.println("Programador"); // exibe "Programador" no console
	}
}
